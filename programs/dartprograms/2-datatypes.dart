void main(){
  print("data types");
  // int
  int a = 10;
  print("int: $a");
  // double
  // double b = 10.5;
  double b = 10.5;
  print("double: $b");
  // string
  // String c = "hello";
  // String c = "hello";
  // String c = 'hello';
  // String c = '''hello''';
  String c = """hello""";
  print("string: $c");
  // bool
  // bool d = true;
  bool d = false;
  print("bool: $d");
  // list
  // List<String> e = ["hello", "world"];
  List<String> e = <String>["hello", "world"];
  print("list: $e");
  // set
  // Set<String> f = {"hello", "world"};
  Set<String> f = <String>{"hello", "world"};
  print("set: $f");
  // map
  // Map<String, int> g = {"hello": 1, "world": 2};
  Map<String, int> g = <String, int>{"hello": 1, "world": 2};
  print("map: $g");  
}